-- Spring Batch Schema for H2
-- Spring Batch tables are required for job repository functionality

-- Sequences for Spring Batch ID generation
CREATE SEQUENCE IF NOT EXISTS BATCH_JOB_SEQ START WITH 1;
CREATE SEQUENCE IF NOT EXISTS BATCH_JOB_EXECUTION_SEQ START WITH 1;
CREATE SEQUENCE IF NOT EXISTS BATCH_STEP_EXECUTION_SEQ START WITH 1;

-- Batch Job Instance Table
CREATE TABLE IF NOT EXISTS BATCH_JOB_INSTANCE (
    JOB_INSTANCE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VERSION BIGINT,
    JOB_NAME VARCHAR(100) NOT NULL,
    JOB_KEY VARCHAR(32) NOT NULL,
    DATE_TIME TIMESTAMP,
    constraint JOB_INST_UN unique (JOB_NAME, JOB_KEY)
);

-- Batch Job Execution Table  
CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION (
    JOB_EXECUTION_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VERSION BIGINT,
    JOB_INSTANCE_ID BIGINT NOT NULL,
    CREATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    START_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    END_TIME TIMESTAMP,
    STATUS VARCHAR(10),
    EXIT_CODE VARCHAR(20),
    EXIT_MESSAGE VARCHAR(2500),
    LAST_UPDATED TIMESTAMP,
    JOB_CONFIGURATION_LOCATION VARCHAR(2500),
    constraint JOB_EXEC_INST_FK foreign key (JOB_INSTANCE_ID)
    references BATCH_JOB_INSTANCE(JOB_INSTANCE_ID)
);

-- Batch Job Execution Context Table
CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION_CONTEXT (
    JOB_EXECUTION_ID BIGINT NOT NULL PRIMARY KEY,
    SHORT_CONTEXT VARCHAR(2500) NOT NULL,
    SERIALIZED_CONTEXT LONGVARCHAR,
    constraint JOB_EXEC_CTX_FK foreign key (JOB_EXECUTION_ID)
    references BATCH_JOB_EXECUTION(JOB_EXECUTION_ID)
);

-- Batch Step Execution Table
CREATE TABLE IF NOT EXISTS BATCH_STEP_EXECUTION (
    STEP_EXECUTION_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VERSION BIGINT NOT NULL,
    STEP_NAME VARCHAR(100) NOT NULL,
    JOB_EXECUTION_ID BIGINT NOT NULL,
    START_TIME TIMESTAMP NOT NULL,
    END_TIME TIMESTAMP,
    STATUS VARCHAR(10),
    COMMIT_COUNT BIGINT,
    READ_COUNT BIGINT,
    FILTER_COUNT BIGINT,
    WRITE_COUNT BIGINT,
    READ_SKIP_COUNT BIGINT,
    WRITE_SKIP_COUNT BIGINT,
    PROCESS_SKIP_COUNT BIGINT,
    ROLLBACK_COUNT BIGINT,
    EXIT_CODE VARCHAR(20),
    EXIT_MESSAGE VARCHAR(2500),
    LAST_UPDATED TIMESTAMP,
    constraint JOB_EXEC_STEP_FK foreign key (JOB_EXECUTION_ID)
    references BATCH_JOB_EXECUTION(JOB_EXECUTION_ID)
);

-- Batch Step Execution Context Table
CREATE TABLE IF NOT EXISTS BATCH_STEP_EXECUTION_CONTEXT (
    STEP_EXECUTION_ID BIGINT NOT NULL PRIMARY KEY,
    SHORT_CONTEXT VARCHAR(2500) NOT NULL,
    SERIALIZED_CONTEXT LONGVARCHAR,
    constraint STEP_EXEC_CTX_FK foreign key (STEP_EXECUTION_ID)
    references BATCH_STEP_EXECUTION(STEP_EXECUTION_ID)
);

-- Batch Job Execution Parameters Table
CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION_PARAMS (
    JOB_EXECUTION_ID BIGINT NOT NULL,
    PARAMETER_NAME VARCHAR(100) NOT NULL,
    PARAMETER_TYPE VARCHAR(100),
    PARAMETER_VALUE VARCHAR(2500),
    IDENTIFYING CHAR(1),
    constraint JOB_EXEC_PARAMS_FK foreign key (JOB_EXECUTION_ID)
    references BATCH_JOB_EXECUTION(JOB_EXECUTION_ID),
    constraint JOB_EXEC_PARAMS_PK primary key (JOB_EXECUTION_ID, PARAMETER_NAME)
);

-- Custom application tables
CREATE TABLE IF NOT EXISTS batch_records (
    id            BIGINT         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    external_id   VARCHAR(255),
    name          VARCHAR(255)   NOT NULL,
    "value"       DECIMAL(20,4),
    category      VARCHAR(100),
    event_ts      TIMESTAMP,
    record_hash   VARCHAR(64)    NOT NULL,
    job_id        VARCHAR(100),
    partition_idx INTEGER,
    created_at    TIMESTAMP      DEFAULT CURRENT_TIMESTAMP,
    status        VARCHAR(20)    DEFAULT 'LOADED',
    CONSTRAINT uq_record_hash UNIQUE (record_hash)
);

CREATE INDEX IF NOT EXISTS idx_batch_records_job ON batch_records (job_id, partition_idx);

CREATE TABLE IF NOT EXISTS job_file_log (
    file_id          VARCHAR(255)  PRIMARY KEY,
    status           VARCHAR(20)   NOT NULL DEFAULT 'PROCESSING',
    job_execution_id BIGINT,
    started_at       TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,
    completed_at     TIMESTAMP,
    record_count     BIGINT,
    error_message    VARCHAR(2000)
);
